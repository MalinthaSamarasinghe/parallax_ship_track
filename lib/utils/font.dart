/// Inter
TextStyle kInter400(BuildContext context, {Color color = kColorBlack, double? fontSize, double? height, TextDecoration? decoration = TextDecoration.none}) {
  return TextStyle(
    fontFamily: 'Inter',
    color: color,
    fontSize: fontSize ?? 18.sp,
    fontWeight: FontWeight.w400,
    fontStyle: FontStyle.normal,
    decoration: decoration,
    height: height,
  );
}

